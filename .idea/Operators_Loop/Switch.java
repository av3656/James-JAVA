
public class Switch {
    public static void main(String[] args) {
        int num = 22;
        switch (num) {
            case 21:
                System.out.println("Case 21");
            case 22:
                System.out.println("Case 22");
            case 23:
                System.out.println("Case 23");
            case 24:
                System.out.println("Case 22");
            case 25:
                System.out.println("Case 22");
        }
    }
}
// This given code is an example of Switch case.